    <div class="article__body" itemprop="articleBody">
      <p>During the last decade our clients have leaped into digital business. Together we have experienced huge successes and also some major flops. This is my summary of what we have learned. The success factors in building digital services seem to be recurring and possible to summarize in one paragraph. Let me try!</p>

<blockquote>
<p>Make sure you identified a problem worth solving. Find a passionate owner for it. Take an even more passionate team that breathes the same air with its customers, a team cross-functional, co-located and skilled enough to deliver awesomeness. Add a good methodology (and some money). Let them work hard, iterate and converge into success... or fail fast.</p>
</blockquote>

<p>Our work is based on teachings of several modern disciplines: <a href="http://agilemanifesto.org/">agile software development</a>, <a href="http://thisisservicedesignthinking.com/">service design</a> and <a href="http://en.wikipedia.org/wiki/User-centered_design">user-centered design</a>, <a href="http://en.wikipedia.org/wiki/Lean_manufacturing">lean</a>, and <a href="http://en.wikipedia.org/wiki/Analytics">analytics</a>. These share many core principles such as autonomous, co-located, cross-functional teams and embracing change, but they have their origins in different professions and cultures. Actual contradictions are rare but the language is different and the interfaces are hard. Take “empirical evidence". In application analytics it is looking into data. In lean startup it is the process of validation, no matter what the tools. For usability folks it is the user tests. For devops it is a green monitoring screen. For business it is the bottom line. I am sure you got it: they all have their point.</p>

<p>Evidence must be our only guiding star, but it does not mean the same to everybody. We need to learn to understand the languages of all disciplines.</p>

<h2>Digital innovations are born in startups only?</h2>

<p>Small teams and tight resources have proven to be a superior way of building new services. Fast validation on the market beats up-front planning hands-down. Traditional stage-gate product development, budgeting and siloed functions like marketing are hard to justify. But to turn the ways of working upside-down – and potentially cannibalizing your own business at the same time – is truly hard.</p>

<p>However, many leading companies like Sanoma and Kone are determined to try. Their top-management supports intrapreneurship and they have launched initiatives to build innovation engines and accelerator programs inside the corporation. Google is an inspiring example of a large corporation that has retained its innovation capability.</p>

<p>Digital transformation is a billion-euro opportunity for enterprises in all industries. To succeed we need to learn from the big and small winners of the digital world. But this is not just their game. Organisations on various maturity levels can benefit from the modern methodologies. Agencies and their customers need to work together in teams who walk the path from problem validation all the way to github and marketing together without handovers. They need a Rosetta stone of digital services creation to communicate.</p>

<p>To tackle all this a holistic, lean way of continuous designing, building and also operating services is needed. We decided to call it Lean Service Creation.</p>

