<p><em><a href="">Jaane Rosendahl</a> is a business developer with a passion for customer-centricity, brands, company cultures and customer economics. For 50% of her 30-year career, she has worked to help companies embrace the digital. </em></p>

<p>Digital technogy is awesome! It makes the impossible possible. I remember being one of the lucky ones who got a mobile phone in the early 90’s. Could there be a more wondrous device? And emails? I came to work after maternity leave and it was there! What a difference! The speed, reach and simultaneity of it! Not to mention text messaging! The world had changed. We had stepped into the world of communication. It’s a world in which we are – as companies – still struggling to find our way.</p>

<p>After the early advances in companies, digital has changed our behavior in our personal lives and in our homes faster and more profoundly than in our working environment. We expect more from the digital services we use in our personal lives than the ones we use at work.</p>

<h3 class="article__heading--h3">A digital gulf</h3>

<p>There is a digital separation. How on earth did this separation come around? What we take for granted at home and expect from the services we personally use doesn’t follow us to work. We go to our workplaces in traditional companies ready to fight every reform with familiar arguments:</p>

<ul>
	<li>“Not in our business…”</li>
	<li>“Our customers don’t want to…”</li>
	<li>“Our business is different…”</li>
</ul>

<p>What we expect from other companies’ services, doesn’t apply to us:</p>

<ul>
	<li>“There are legal obstacles…”</li>
	<li>“Our IT systems are too complicated…”</li>
	<li>“Personal service will always be the only relevant touch point in our business…”</li>
</ul>

<p>Company leaders nod at digitalization and accept its winning streak as a fact. They include it in their strategy, but – at worst – they boast about not being on any of the social media channels themselves. They feel they’re above it, even though that’s where their customers are. Why did the development of digital services become such a pain in the ass? An anxious MUST? The simple answer is that companies do not possess the capabilities and tools necessary to lead and organize digital business. Business leaders try to stuff the digital in the frames of their old business. They are stuck in the procedures from production economics but digital business is born in the era of customer economics.</p>

<h3 class="article__heading--h3">Pitfalls to avoid</h3>

<p>In my experience, these are some of the mistakes to avoid when organizing the digital. It’ll help some of the much-needed joy in development:</p>

<ol>
	<li>
<em>Do not hand responsibility for a digital project to the wrong person.</em> Any digital project leader, project manager, expert or team has to believe in what is being done. If they are not passionately enthusiastic about it, they’ll see nothing but obstacles. The barriers and hindrances will come up during the project no matter what, so problems are not a good place to start. Spreading digital doesn’t happen along traditional lines. Maintenance managers and organizations are not the best environments for implementing new services and ways of working.<br>
	<br>
	<strong>Enable success.</strong><br>
	 </li>
	<li>
<em>Do not mix two different issues: creating new digital services for customers and automating old legacy systems.</em> It just doesn’t work. The UX/customer interface people and traditional IT people march to the beat of different drums. Start developing digital services from the customer's point of view and proceed from there. You can always find competent help to solve technological issues. Don’t work on legacy terms. It’ll just make you slow and cranky. Trial and error and analyze and learn and trial …<br>
	<br>
	<strong>Enable progress.</strong><br>
	 </li>
	<li>
<em>Don't be democratic.</em> In a traditional company most of your management team is… well, traditional. Voting doesn’t produce the right decisions. Your CDO or whoever has the abilities and enthusiasm to take that responsibility should have more votes. And don’t leave your CDO, or person filling a similar niche, to fight the battle alone.<br>
	<br>
	<strong>Enable innovative decisions.</strong><br>
	 </li>
	<li>
<em>Don't forget to develop your brand and culture accordingly.</em> How will digitalization affect your identity, how should it manifest itself in your culture? Take small measures in your everyday work in the organization. The best way to learn is to do it yourself – in a relevant way. Provide digital pioneers and the rest of the organization opportunities for continuous interaction. Build curiosity into your culture. That way understanding grows and insights multiply. Communicate, communicate, communicate.<br>
	<br>
	<strong>Enable curiosity that breaks barriers. </strong><br>
	 </li>
	<li>
<em>Do not measure the wrong things.</em> Measure the effects you want to have an impact on. Assign relevant indicators for success and failure. Why did you start the project? Why is it a part of your strategy? Don’t let the organization valuate progress, each and everyone using their own metrics.<br>
	<br>
	<strong>Enable focus.</strong><br>
	 </li>
	<li>
<em>Do not believe for a second that digitalization is going to proceed smoothly by itself after it has been organized.</em> It won’t. New endeavors need continuous attention and care. Otherwise they wither under the weight of the dominant traditional thinking.<br>
	<br>
	<strong>​Enable long-term change.</strong>
</li>
</ol>

<p>We should make digitalization today’s voyage of discovery. A lot of curiosity, barrier-free thinking, agile planning, use of scouting, oodles of common sense and a touch of craziness.</p>

<p>Written by Jaana Rosendahl</p>

