      <p dir="ltr">Being a great place to work is not easy, but the real challenge stems from the fact that people—especially the young and talented people—are not very well equipped to deal with the status quo. Which is to say, many people are only happy when things are improving.</p>

<p dir="ltr">Constant improvement being what any organisation should strive for, what’s the problem?</p>

<p dir="ltr">Let me rephrase; people are only <em>really happy</em> when things are <em>really improving</em>. The higher you climb on the tree of happiness, the more difficult it becomes to find any significant improvements to work life.</p>

<p dir="ltr">Here’s a chart that is not based on real data. I just drew it to prove my point, as this is how you do it in 2016:</p>

<p> </p>

<p dir="ltr"><img alt="chart-worklife-quality-paint-blur.png" src="../assets/images/res/chart-worklife-quality-paint-blur.png"></p>

<p> </p>

<p dir="ltr">The point I am flawlessly proving is that happiness decays. Keeping up with that decay easily becomes a Red Queen’s race.</p>

<p dir="ltr" style="margin-left:80px"><em>"Well, in our country," said Alice, still panting a little, "you'd generally get to somewhere else—if you run very fast for a long time, as we've been doing."</em></p>

<p dir="ltr" style="margin-left:80px"><em>"A slow sort of country!" said the Queen. "Now, here, you see, it takes all the running you can do, to keep in the same place. If you want to get somewhere else, you must run at least twice as fast as that!"</em></p>

<p dir="ltr" style="margin-left:80px"><em>-- Lewis Carroll</em></p>

<p dir="ltr">While your organisation climbs up this Great Employer ladder, it soon becomes insufficient just to come up with new perks and avoid upsetting anyone.</p>

<figure class="article__figure">
<div class="article__figure__placeholder"><a class="article__figure__link" data-type="image" href="../assets/images/res/bmx-bike-small_s1800x0_q80_noupscale.png" target="_blank"><img alt="A picture of a BMX bike" src="../assets/images/res/bmx-bike-small_s830x0_q80_noupscale.png"></a></div>
</figure>

<p dir="ltr" style="margin-left:80px"><em>Anecdote! </em><em>When I was 11 years old all I wanted for Christmas was a BMX bike.</em></p>

<p dir="ltr" style="margin-left:80px"><em>It was strongly implied by my campaign that the future lacking a BMX bike would not be a pleasant one to anyone. So I got one. The next day I took it out for a spin (yes, in the snow) and immediately encountered my neighbourhood nemesis riding a similar, but more expensive brand-new bike.</em></p>

<p dir="ltr" style="margin-left:80px"><em>I was devastated. After 27 years I have still not entirely forgiven the universe this betrayal. I had received a great benefit, but my rival had it better, rendering mine absolutely worthless.</em></p>

<p dir="ltr">Sensible adults, however, are capable of shrugging off such setbacks quickly. Yet a lot of effort is expended by companies to make sure nobody gets upset about anything. This fear of disappointments often cripples efforts to gain significant improvements.</p>

<p dir="ltr"><em>We can’t try this, as someone might not like it. You can’t do that, as someone may consider it unfair. </em>It’s a familiar song.</p>

<p dir="ltr">So it’s easy to end up playing it safe, getting better coffee, handing out freebies to the employees. The ambition is misdirected and the company keeps investing an increasing amount of energy in trying to come up with new perks that do not offend anyone, and sound better than what the competitors have.</p>

<p dir="ltr">Here’s an amusing quote from a <a href="http://fortune.com/best-companies/google-alphabet-1/">recent article by Fortune</a>:</p>

<p dir="ltr" style="margin-left:80px"><em>The company culture truly makes workers feel they're valued and respected as a human being, not as a cog in a machine. The perks are phenomenal. From three prepared organic meals a day to unlimited snacks, artisan coffee and tea to free personal-fitness classes, health clinics, on-site oil changes, haircuts, spa truck, bike-repair truck, nap pods, free on-site laundry rooms, and subsidized wash and fold. The list is endless.</em></p>

<p dir="ltr">No mistake, quality coffee and free movie tickets are <em>sweet</em>, but they do not form a viable long-term tactic to manage the happiness decay of ambitious people. Nor do the haircuts, bike-repairs, or… what the hell is a spa truck? I bet it doesn’t help either, though now I kind of want one for Futurice.</p>

<p dir="ltr">Having my employer shower me with services and gifts gives me a quaint satisfaction of being royalty, of being special. But it is a fleeting feeling. Nothing suffices, you get used to all the perks and start to take them for granted.</p>

<p dir="ltr" style="margin-left:80px"><em>Speculation and some hyperbole!</em></p>

<p dir="ltr" style="margin-left:80px"><em>Are the real royalty or the super rich ever really satisfied with anything, I wonder? Muhammad Mahabat Khan, the Nawab of Junagadh, arranged a grand wedding celebration of his favourite pet dog Roshanara with a golden retriever Bobby. His people were starving. He invited 50000 guests. After the ceremonies, did he kick back and feel complete? No, he acquired 500 more dogs and spent 11% of the entire state’s income to cater for them.</em></p>

<p dir="ltr">There are tools that claim to help you achieve tranquil inner peace. Self-help books on life acceptance. Endless articles in the web: <em>“3 ways to be happy”, “7 ways to be happier”, “The 25 habits of the punchably happy people”</em>. Mindfulness is trending. Some people still advocate psychedelic drugs or religion. I don’t know what Bob Ross’s secret was, but it seemed to be working for him. Maybe he had a spa truck?</p>

<p dir="ltr">I haven’t really tried any of these and I remain sceptical.</p>

<p dir="ltr">There is, however, something else that a company can do. We can help the employees to advance a good cause, such as open source, technology, science, education, social equality, or sustainability.</p>

<p dir="ltr">If the employee can have the time and company support to pursue something that brings out her passion, it will do a lot more than a crazy variety of perks.</p>

<p dir="ltr">The "help the employees"-part of the statement above is <em>very important</em>. People have causes they want to advance. The company can and should support them in that, if the cause is good.</p>

<p dir="ltr">The traditional corporate social responsibility approach has the company deciding and declaring the cause and then just throw some money at it. That may help with PR, but it’s not going to help with keeping the people happy and motivated.</p>

<p dir="ltr">How do you assess if the cause is good? We use a simple rule. The activities we sponsor must benefit the persons doing them, the company, and the society. Open source is an obvious example, and I <a href="year-2015-in-company-sponsored-open-source.html">wrote about the benefits earlier</a>.</p>

<p dir="ltr">People get acclimated to whatever level of luxury they get, but you never get tired of having more purpose or meaning in your life. That is why employers should support their people in doing good. Every party benefits and our world is saved in the process.</p>

