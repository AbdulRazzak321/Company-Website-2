        Mobile app makes customers&#x27; daily lives easier -  Korkster
      </h1>
    </header>


    <div class="article__body" itemprop="articleBody">
      <p>Kesko is one of Finland's  two major grocery store chains, with a 33.1 % share of the 16.7 billion € grocery retail market (2014). 900 000 customers visit the chain's 900 grocery stores every day. Kesko also operates hardware stores and a car sales business.</p>

<h2 class="article__heading--h2">Challenge</h2>

<p>Providing a wide range of digital services to help make the customers' daily lives easier is a key strategic focus for Kesko's grocery retail business. The chain's web presence is strong and Kesko wanted to develop their mobile channel, too. The K-ruoka mobile app's stated mission is to make the daily lives of their grocery store customers easier by providing them with intelligent shopping tools.</p>

<figure class="article__figure">
<div class="article__figure__placeholder"><a class="article__figure__link" data-type="image" href="../assets/images/work/korkster.png" target="_blank"><img alt="K-ruoka mobile app" src="../assets/images/work/korkster.png"></a></div>
</figure>

<h2 class="article__heading--h2">Approach</h2>

<p>A service vision sprint with a service designer, a visual designer and technical architects for mobile client and backend kicked off the project. The sprint outlined the service's business objectives, key performance indicators, design drivers, customer journey and the role of the application in it, as well as the key use cases and other requirements.</p>

<p>Based on this understanding the team determined the application’s preliminary structure and interaction logic, visual style and technical specifications. The vision was validated in interviews with potential end-users and shopkeepers.</p>

<p>A prioritised product backlog was derived from these requirements and a full-scale iterative design and development phase ramped up soon after. Agile methodologies and validation of the solution with end users allowed the cross-functional team of Futurice service creators deliver a multi-platform mobile application (iOS, Android, Windows Phone) that meets both the end-users' needs and Kesko's business objectives.</p>

<p>Futurice also delivered a marketing video to support the launch.</p>

