<h2 class="article__heading--h2">Impact</h2>

<p>Two days from launch, the application was at the top of the free apps chart on App Store. The current version of the application has an average rating of 4.5/5 in App Store.</p>

<p>Based on the analytics implemented within the application, users have used the application regularly as part of their daily lives. The application is fulfilling its purpose.</p>

<p>Android and Windows Phone versions of the application will be released soon and after that, Kesko will continue iterative development of the application to achieve even better customer satisfaction and business performance.</p>

<p><em>“Futurice’s agile way of working and the ability to create products that appeal to users helped us take better advantage of the deep understanding we have of our customers’ needs.”  </em><strong>Mika Kakko, E-commerce Sales Director at Ruokakesko Oy</strong></p>

