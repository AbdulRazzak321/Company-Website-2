      <h1 class="article__header__title" itemprop="headline">
        Digital capabilities and innovation culture for a ferry company - WalknSell
      </h1>
    </header>


    <div class="article__body" itemprop="articleBody">
      <figure class="article__figure">
<div class="article__figure__placeholder"><img alt="source: AS Tallink Grupp​" src="../assets/images/work/walknsell.png"></div>

<figcaption><span class="article__figure__caption">source: AS Tallink Grupp​</span></figcaption>
