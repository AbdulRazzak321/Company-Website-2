<p>Tallink is the market leader in leisure and business travel in the Baltic Sea area, with a 46% market share of the passenger market. Over 8.8 million passengers traveled on Tallink ferries in 2014. Based on revenues, it is the third largest ferry operator in the world. On-board sales are an important part of the business, comprising 50% of revenues. Tallink also provides hotel, travel, cargo and transportation services. </p>

<p>More info on Tallink: <a href="http://www.tallink.com/" target="_blank">www.tallink.com</a> </p>

<p><strong>Problem statement</strong></p>

<p>The Tallink management board wanted an independent review of the current state of digital development in their organisation. They wanted to identify ways to speed up projects and set up innovation processes to meet business needs - all necessary to build the digital capabilities that enable a better customer experience and personalization of the offering, while providing up &amp; cross-sell opportunities as well as optimizing costs through self-service.</p>

<p>Tallink’s software development environment is quite complex. Several, often physically separated teams work on development projects. Project timelines and change management are critical challenges. Finding the best solutions to meet both IT and business needs is not easy, either.</p>

<figure class="article__figure">
<div class="article__figure__placeholder"><img alt="source: AS Tallink Grupp​" src="../assets/images/res/small_ts_seaview_002--tphy9m1sjx_s830x0_q80_noupscale.jpg"></div>

<figcaption><span class="article__figure__caption">source: AS Tallink Grupp​</span></figcaption>
</figure>

<p>For an organisation to respond to a changing business environment, it must change the way it works. New problems require novel solutions.</p>

<p>What Tallink had was a strong desire to learn and change, and an ambition to be a pioneer in their industry. What they needed was a better understanding of the current state of their organisation, as well as the tools to improve it.</p>

<p><strong>Approach</strong></p>

<p>A three-person core team from Futurice, representing the technology, design, business competences, worked on-site in Tallinn, Estonia.  </p>

<ul>
	<li>Project duration: 7 weeks</li>
	<li>Interviews with 28 people across the organization</li>
	<li>Three projects in more detail</li>
	<li>Value stream mapping workshops with project teams</li>
	<li>Technology landscape review</li>
</ul>

<p>All major stakeholders were involved. Various aspects of the innovation process were covered, including organisation, culture, technologies, tools, and partners.</p>

<div>
<p><strong>Impact</strong></p>
</div>

<p>Tallink has very skilled and motivated people who can transform the company and establish it as a benchmark for utilizing digital in the travel industry. To unleash the potential and create an environment that enables Tallink to succeed in this, we worked on the following assets:</p>

<ul>
	<li>Sufficient understanding for starting a digital change program</li>
	<li>Perspective from a partner with experience delivering successful projects</li>
	<li>Analysis and documentation of the current state</li>
	<li>Organization and project level recommendations</li>
	<li>Engagement from both management and project teams</li>
</ul>

<p><em>“The team from Futurice was professional in conducting the analysis and produced results that can help us speed up our digital development.” </em><br>
<strong>Janek Stalmeister, CEO, Tallink Group</strong></p>

