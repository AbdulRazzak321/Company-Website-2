    <div class="article__body" itemprop="articleBody">
      <p>We are Futurice: the leanest, meanest (not really, we are nice! It's one of our superpowers) and one of the most international design &amp; tech agencies in Europe. We use human-centered design, the latest innovations in software and and advanced analytics to create digital marvels that make our clients go nuts and their competitors green with envy! Join us now in Stockholm.</p>

<p>As Tech Lead you're the most senior software developer at our new office. You're responsible for shaping the core technical competencies, while putting your stamp on the design and vision of our new office.</p>

<h4 class="article__heading--h4">What you can expect to do</h4>

<ul>
	<li>Develop innovative software solutions and integrate them into our customers’ software landscape</li>
	<li>Design and implement software architectures</li>
	<li>Lead projects and develop concepts together with our customers</li>
	<li>Technical consulting in close cooperation with other developers, designers, data scientists and digital business people</li>
	<li>Create solution proposals and presentations for our customers</li>
	<li>Assist establishing the Stockholm office</li>
	<li>Recruit new employees and mentor the existing ones</li>
	<li>Create a buzz in the market about how amazing you and the company you work for (that's us!) are</li>
</ul>

<h4 class="article__heading--h4">What we expect</h4>

<ul>
	<li>A passion for software</li>
	<li>Several years of professional experience in software development, ideally in and around web and mobile - more exotic experiences are a big plus!</li>
	<li>An architecture-level understanding of software design plus experience with implementing multi-layer services</li>
	<li>Experience in designing and implementing modern REST-based APIs, using e.g. Java, Scala, or Node.js</li>
	<li>Good understanding of both relational databases as well as no-SQL databases</li>
	<li>A broad knowledge of tools, frameworks and technologies related to designing, implementing, deploying and hosting software services</li>
	<li>Strong expertise in agile software development and methods (e.g. Scrum, Kanban)</li>
	<li>Experience in staff management and career development</li>
</ul>

<p>Your analytical skills are solid. Your excellent communication skills allow you to collaborate with motivated people with a wide variety of skills, coming different backgrounds. You're fluent in English, our company language. Swedish is an obvious advantage.</p>

<h4 class="article__heading--h4">What we offer</h4>

<ul>
	<li>The opportunity to take part in creating the vision and design of our new office in Stockholm</li>
	<li>An enthusiastic, extremely capable, helpful, cooperative and international team of colleagues</li>
	<li>A flexible working environment with short decision paths and no hierarchies</li>
	<li>Exciting projects with renowned customers</li>
	<li>Your choice of tools </li>
	<li>Nice perks and more!</li>
</ul>

<p>We can offer you a great workplace with awesome colleagues, inspiring projects and the option of working at Futurice offices abroad for shorter or longer stints as well as world-class tools and excellent benefits. We are proud of our lean, flat organization and distinctive culture, and were chosen as the best place to work in Europe - twice. Join us and become part of an international, highly transparent company culture with lots of exciting career opportunities!</p>

<p>Here's a peek at our <a href="../careers/stockholm.html">thinking</a>.</p>

<p>Act soon. The role will be filled when we find just the right person.</p>

