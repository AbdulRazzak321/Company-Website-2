    <div class="article__body" itemprop="articleBody">
      <p dir="ltr">You think in systems, services and API’s? A browser is not just a rendering tool, it's your medium of choice? Whether it's JavaScript, Java, Python, Clojure, .NET, or Scala - web is your thing. It's not just about getting the data to flow from the database to the user's screen. It's about getting to know the technologies, understanding the layers and finally offering a great user experience.</p>

<p dir="ltr">Do you want to be challenged by your work, and learn from equally skilled and passionate colleagues? Get in touch!</p>

<p dir="ltr"><strong>What awaits you:</strong></p>

<ul>
	<li>Concepting digital services as part of an interdisciplinary team</li>
	<li>Fun and challenging projects with clients from many industries</li>
	<li>Development of a wide variety of web applications and services ranging from rich client applications to complex web services and API’s</li>
	<li>Sharing experiences and learnings with your colleagues</li>
	<li>Constant learning and self-improvement through conferences and team events</li>
	<li>Flat hierarchies and the opportunity to make an impact</li>
</ul>

<p><strong>What we expect:</strong></p>

<ul>
	<li>Strong background in front-end development (Javascript, CSS, HTML)</li>
	<li>The passion and skill required to build and ship high-quality software in a an agile team</li>
	<li>A clear understanding of configuring build systems (Grunt, Gulp, etc.)</li>
	<li>Experience with at least two major client-side frameworks (Backbone, Angular, React, etc.)</li>
	<li>Interest in other platforms and technologies, as well as future trends</li>
	<li>Reactive frameworks and modern architectures (MVP/MVC/MVVM) are a plus</li>
	<li>Experience with agile development practices</li>
	<li>Familiarity with Android’s testing tools and judgment to write concise, effective unit tests</li>
	<li>The ability to discuss project work with our customers, over phone, video conference, or at our or their premises. Some travelling may be required</li>
</ul>

<p dir="ltr"><strong>For a senior role, you should also have:</strong></p>

<ul>
	<li>Over 5 years of web development experience</li>
	<li>Interest in mentoring junior team members</li>
	<li>A big picture mentality to support solving problems from client, to server and system architecture.</li>
	<li>Experience with cloud technologies and architectures (AWS, Docker, etc)</li>
	<li>Experience with test automation, continuous integration/delivery and unit testing frameworks</li>
	<li>You are able to communicate fluently in English, our company language. German skills are beneficial, but not required. You should have a passion for learning and the talent to quickly grasp new concepts and technologies.</li>
</ul>

<p dir="ltr"><strong>What we offer you:</strong></p>

<p dir="ltr">We are a team of enthusiastic and ambitious people from a dozen different countries that aim to create a caring and inclusive workplace. We foster continuous improvement, learning and support people that want to spread their knowledge by teaching and giving back to the community. We love Munich for its great quality of life and because it is home to some of the most advanced technology companies in the world, who work on some of the most ambitious and challenging projects. If you want to experience other cities you also have the opportunity to work at our other sites, e.g. Berlin, London or Helsinki, through client projects or our FutuHike program. Futurice also offers <a href="http://spiceprogram.org/oss-sponsorship/">sponsorships for open source contributions</a> that you do in your free time. And since you know best what tools you need, you get to pick your work laptop, smartphone, tablet or smartwatch.</p>

<p dir="ltr">Join us and become part of an international, <a href="../blog/the-many-benefits-of-transparency.html">highly transparent</a> company culture with lots of exciting career opportunities!</p>

