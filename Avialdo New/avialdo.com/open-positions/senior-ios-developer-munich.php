    <div class="article__body" itemprop="articleBody">
      <p>Are you are a passionate iOS developer who wants to have an impact on people's lives, loves fresh challenges and enjoys life on the edge? Do you want to be challenged by your work, and learn from equally skilled and passionate colleagues? Get in touch!</p>

<p><strong>What awaits you:</strong></p>

<ul>
	<li>Concepting digital services as part of an interdisciplinary team</li>
	<li>Fun and challenging projects with clients from many industries</li>
	<li>Development of top-notch native apps for iOS and watchOS</li>
	<li>Sharing experiences and learnings with your colleagues</li>
	<li>Constant learning and self-improvement through conferences and team events</li>
	<li>Flat hierarchies and the opportunity to make an impact</li>
</ul>

<p><strong>What we expect:</strong></p>

<ul>
	<li>A background in iOS or OS X development (Swift, Objective-C)</li>
	<li>Strong command of the latest SDK’s and developer toolsets (Xcode, AppCode, Instruments)</li>
	<li>The passion and skill required to build and ship high-quality software</li>
	<li>Interest in other platforms and technologies, as well as future trends</li>
	<li>Experience with Swift, reactive frameworks (e.g. Rx, ReactiveCocoa) and modern architectures (e.g. MVVM, VIPER) is a plus</li>
	<li>The ability to discuss project work with our customers, over phone, video conference, or at our or their premises. Some travelling may be required</li>
</ul>

<p><strong>For a senior role, you should also have:</strong></p>

<ul>
	<li>Over 5 years of mobile development experience</li>
	<li>Interest in mentoring junior team members</li>
	<li>A big picture mentality to support solving problems from client, to server and system architecture.</li>
	<li>Experience with test automation, continuous delivery and unit testing frameworks (e.g. XCTest, Kiwi or Quick)</li>
</ul>

<p>You are able to communicate fluently in English, our company language. German skills are beneficial, but not required. You should have a passion for learning and the talent to quickly grasp new concepts and technologies.</p>

<p><strong>What we offer you:</strong></p>

<p>We are a team of enthusiastic and ambitious people from a dozen different countries that aim to create a caring and inclusive workplace. We foster continuous improvement, learning and support people that want to spread their knowledge by teaching and giving back to the community. We love Munich for its great quality of life and because it is home to some of the most advanced technology companies in the world, who work on some of the most ambitious and challenging projects. If you want to experience other cities you also have the opportunity to work at our other sites, e.g. Berlin, London or Helsinki, through client projects or our FutuHike program. Futurice also offers <a href="http://spiceprogram.org/oss-sponsorship/">sponsorships for open source contributions</a> that you do in your free time. And since you know best what tools you need, you get to pick your work laptop, smartphone, tablet or smartwatch.</p>

<p dir="ltr">Join us and become part of an international, <a href="../blog/the-many-benefits-of-transparency.html">highly transparent</a> company culture with lots of exciting career opportunities!</p>

<p> </p>

