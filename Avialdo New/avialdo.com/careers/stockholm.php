<p class="article__paragraph">We’re in a bit of a pickle. A jam, even. We’re looking for people, but not just anyone will do. <strong>We’re looking for a very particular type: an independent-minded team player who loves to get things done.</strong> </p>

<p class="article__paragraph">We're doing awesome business in Berlin, London, Helsinki and Tampere. Now we're opening an office in Stockholm. This means plenty of new opportunities. We need pioneers, team builders and world-class technologists who are ready to make a change.</p>

<p class="article__paragraph"><strong>We need you.</strong> Our shiny new Stockholm site offers you a very real opportunity have an impact on our company in Sweden and internationally.</p>

<p>You’ll be building a business from the ground up and leaving your mark on it.</p>

<p>You’ll have all the support an international creative tech agency active in four different countries can offer, but without corporate meddling. We hire smart people and trust their judgement.</p>

<blockquote>
<p>Do you want to grow with and learn from the best people working in technology today?</p>
</blockquote>

<p>You’ll be launching a proven and successful way of doing things and instigating cultural change in client organizations to a new market. The demand is there.</p>

<figure class="article__figure">
<div class="article__figure__placeholder"><a class="article__figure__link" data-type="image" href="../assets/images/res/img_1751_s1800x0_q80_noupscale.jpg" target="_blank"><img alt="" src="../assets/images/res/img_1751_s830x0_q80_noupscale.jpg"></a></div>
</figure>

<p class="article__paragraph">Our success as a company is built on our unique culture. It’s open and transparent. It prices the sharing of knowledge above almost everything else, with the exception of the well-being of the people who work here. This culture has allowed us to create tools and methods that help some of the biggest companies in the Nordics and Europe thrive in a rapidly changing business environment.</p>

<blockquote>
<p class="article__paragraph">Do you want to help build the best tech team in Sweden?</p>
</blockquote>

<p>Important: as you read the job ads below, keep in mind that they’re just examples and we’re really looking for people, not line items on a resumé. If you’re willing to grow, we’ll support you in the process. You’ll work with bleeding edge tech, using the best available methods, with the full backing of our world-class developers.</p>

