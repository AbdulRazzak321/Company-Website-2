<p><a href="../people/martin-richter.html"><strong>Martin Richter</strong></a>, a half-Finn, half-German software craftsman, started working for Futurice in 2011. He is now working at Futurice’s Berlin Office. As he says:</p>

<blockquote>
<p>After 14 years as a German in Helsinki, and with a freshly-baked M.Sc. in Robotics and Computer Science, I chose to become a Finn in Berlin.  </p>
</blockquote>

<p>Martin’s career has taken a couple of interesting turns, including a three-month internship in Northern India. </p>

<blockquote>
<p>I felt that a straightforward academic career wasn't for me, so after getting my B.Sc. I travelled to India to work for a small software company. It was a very interesting and eye-opening experience: they had a very strong hierarchy, employees had no power to make decisions and they had all these mindless rules. For example, our bosses prevented us from going on the Internet during working hours, because they thought we were using Facebook. And we were developing web sites! My experiences in India were the complete opposite of what working at Futurice is like.</p>
</blockquote>

<h2 class="article__heading--h2">From biorobotics to media technology </h2>

<p>Martin says he didn’t have a lot of experience with coding before his studies. At Aalto University he studied Bioinformation technology, but took a lot of coding classes on the side.</p>

<blockquote>
<p>Bioinformation technology is very interesting; it involves developing sensors for the human body in all kinds of use-cases, mostly for handicapped people or prosthetic limbs. It was cool stuff, but I missed programming. I did some technical courses and a minor in media technology.</p>
</blockquote>

<p>Lately, Martin’s been working with iOS Development and Android on several projects. He also runs a couple of web projects as a hobby. Still, Martin tells us he is getting more into service design and wants to understand the full breadth of developed software.</p>

<blockquote>
<p>I think in many ways I’m more of a generalist than a specialist. Of course, I still derive great satisfaction from, say, bending the iOS platform in a new way. Creating a new feature is satisfying. </p>
</blockquote>

<h2 class="article__heading--h2">“Developing services for millions of users motivates me.” </h2>

<p>Working at Futurice has been very satisfying for Martin. He describes the company as a “very friendly environment with very interesting and friendly people”.</p>

<blockquote>
<p>People at Futurice are interested in so many things; we have language fanatics, whiskey fanatics, sports enthusiasts. Variety sets us apart from, for example, small companies or start-ups that attract very like-minded people. I prefer diversity among my colleagues.  We also have a lot of women in the company, even as developers, which is actually pretty rare. </p>
</blockquote>

<p>Most motivating for Martin are Futurice projects that have an impact on millions of users.</p>

<blockquote>
<p>When we build something, it actually has an impact in the user game: our products are guaranteed to be used by real people. This also means that we get to further develop our clients’ products – they’re never done, so to speak.</p>
</blockquote>

<figure contenteditable="false" data-flockler="image-add"><img alt="Martin on Google Hangout​​" src="../../flockler.com/thumbs/2003/martin2_s830x0_q80_noupscale.jpg">
<figcaption><span class="image-caption-title" contenteditable="false">Martin on Google Hangout​​</span></figcaption>
</figure>

<p>Still, Martin admits, that as a company, Futurice is not always in a position to to deliver everything it could. He says this is a question of money, communication or other issues. </p>

<blockquote>
<p>People should be given the chance to use their strengths more, to really shine! This is something everybody has to work on together, every day. And the same applies to the client’s side, too. People are not always in the same flow.</p>
</blockquote>

<p>Martin feels that he’s developed his skills on many levels after starting to work for Futurice.</p>

<blockquote>
<p>When writing software on my own, there are no constraints, I don’t have to think about anything except my own time. At Futurice, I’ve learned to focus my effort in areas that have real value. Being surrounded by really good people helps me do this. I’m personally learning new things in every project. Futurice gives people the freedom to develop themselves. I think people are smart and if you don’t get in their way, they’ll know what to do.</p>
</blockquote>

<p>And what’s the next big thing Martin would like to be very good at?</p>

<blockquote>
<p>I’d like to put my knowledge into a larger context. Sometimes as a developer, it’s easy to dig a hole for yourself (Finnish expression: ”kaivaa potero”) and just do your own small part of the project.  I’d like to look at the whole landscape of the project I’m working on and maybe consult with someone about some ideas. I’d like to help our customers know what the future is like and then sell them a solution for it. </p>
</blockquote>

