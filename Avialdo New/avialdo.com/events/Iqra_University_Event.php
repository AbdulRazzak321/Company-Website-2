<p><strong></strong></p>

<p>We all need to test things out from time to time - so here's your opportunity to do some lean testing and hear product dev war stories from Culinar, Flockler, Usetrace, Zervant and Futurice.</p>

<p>We welcome all startups, teams, students and like-minded characters to join Beer &amp; Beta, because testing stuff, playing with your peers' MVPs, getting some extra inspiration and meeting awesome people is cool.</p>

<p>Did we mention that all this happens over a couple of beers? Non-alcoholic beverages also provided.</p>

<p><strong>Event format</strong><br>
We'll start with insightful presentations from our guests and then introduce each fabulous startup on the testing track. Then you can start fiddling and testing all the products.</p>

<p>The event is free, but registration is required so we can reserve enough beer for everyone.<br>
<br>
<strong>How can you get on the testing track?</strong><br>
If you want your startup to get on the testing track, email sointu@flockler.com and we'll make it happen. We have 10 openings. Bring your idea, beta-version or somethign that's already in the markets. We guarantee valuable insights.</p>

