<p><span><span>The best decisions are made out in the field with the most information available. Still, in a traditional hierarchical organization, decisions are often made on the top floor.</span></span></p>

<p dir="ltr"><span><span>At Futurice, everyone has the power to decide and the obligation to make good, transparent and fair decisions. To support that, we created the 3x2 model.</span></span></p>

<p><span><span>When moving towards a decision, a new project or an outsourcing task, there are three (3) times two (2) aspects to consider about its immediate and future impact: how does the decision affect your colleagues, the customer and the numbers, now and in the future.</span></span></p>

<p>As our Culture Director <strong>Hanno Nevanlinna</strong> would describe it: </p>

<blockquote>
<p dir="ltr"><span>"We trust our people to make the best decisions themselves. Nobody else can know better what they should be doing."</span></p>
</blockquote>

